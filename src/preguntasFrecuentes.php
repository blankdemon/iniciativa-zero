<p><b>1) <a name="a-1" id="a-1"></a>What is Network  Marketing?</b></p>
<p>Network  Marketing is a type of business very popular with people looking for part-time,  flexible business. Some of the best-known companies in America, including Avon,  Herbalife, Mary Kay Cosmetics and Tupperware, fall under the network marketing  umbrella. Network marketing programs feature a low up front investment -usually  only a few  dollars for the purchase of a product sample kit- and the  opportunity to sell a product line directly to friends, family and other  personal contacts. Most network marketing programs also ask participants to  recruit other sales representatives.</p>
